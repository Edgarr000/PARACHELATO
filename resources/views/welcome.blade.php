        <h1>¿Qué es una red? </h1>
        <small> <strong> 29 de febrero 2024 por Perla Edelmira Ucles MOncada</strong></small>
        <img src="img/conexiones.jpg.jpg" alt="">
        <p>En informática, se entiende por red (usualmente red informática o red de computadoras) a la interconexión de un número determinado de computadores (o de redes, a su vez) mediante dispositivos alámbricos o inalámbricos que, mediante impulsos eléctricos, ondas electromagnéticas u otros medios físicos, les permiten enviar y recibir información en paquetes de datos, compartir sus recursos y actuar como un conjunto organizado. Las redes cuentan con procesos de emisión y recepción de mensajes, así como de una serie de códigos y estándares que garantizan su comprensión por los computadores conectados a la red (y no por cualquier otro). A dichos estándares de comunicación se los conoce como protocolos, y el más común de ellos actualmente es el TCP/IP.</p>
        <p>La construcción de una red permite administrar una comunicación interna, compartir la ejecución de programas o el acceso a Internet, e incluso la administración de periféricos como impresoras, escáneres, etc. Este tipo de sistemas de enjambre sostienen actualmente muchos de los procesos de administración y procesamiento de información en nuestros días, como son las redes de telecomunicaciones, la Internet o las diversas Intranet empresariales o de organizaciones varias.</p>
        <p>La aparición de las redes revolucionó el modo de comprender la informática y abrió un nuevo campo dentro de esta disciplina para atender las necesidades de mejoría, seguridad y operatividad de la comunicación informática.</p>
