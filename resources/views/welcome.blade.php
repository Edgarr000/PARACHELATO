        <h1>¿Como funcionan las redes?</h1>
        <p>Las redes siguen protocolos, que definen cómo se envían y reciben las comunicaciones. Estos protocolos permiten la comunicación de dispositivos. Cada dispositivo en una red utiliza un Internet Protocol o dirección IP, una cadena de números que identifica de forma única un dispositivo y permite que otros lo reconozcan. </p>
        <img src="img/conectoresj45.jpg.jpg" class="rounded float-start mt-2"
        <p> Las redes de computadoras conectan nodos como computadoras, enrutadores y conmutadores mediante cables, fibra óptica o señales inalámbricas. Esas conexiones permiten a los dispositivos de una red comunicarse y compartir información y recursos.</p>
